# 拟合 y = a*log(x) + b
# 计算 a,b
# 使用最小二乘法：a = Σ((log(x)-mean_logx)*(y-mean_y))/Σ((log(x)-mean_logx)^2), b = mean_y - a*mean_logx
logx = [math.log(ord(c)) for c in known_chars]
mean_logx = sum(logx)/len(logx)
mean_y = sum(y_known)/len(y_known)

